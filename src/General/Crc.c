	uint16_t GetCrc16( uint8_t* pData, uint16_t nLength)
	{
		uint16_t CRC_16;
		uint16_t fcs = 0xffff; // initialization

	   while(nLength>0)
	   {
		 fcs = (fcs >> 8) ^ crctab16[(fcs ^ *pData) & 0xff];
		 nLength--;
		 pData++;
	   }
	   CRC_16  = ~fcs;
	   return CRC_16;

	}
